    private void tabelaLoad() {
        List<RegjistroHuazimet> lista = regjistroHuazimetRepository.findAll();
        regjistroHuazimetTableModel.add(lista);
        tabela.setModel(regjistroHuazimetTableModel);
        regjistroHuazimetTableModel.fireTableDataChanged();
        tabelaSelectedIndexChange();

    }

    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    RegjistroHuazimet regjistroHuazimet = regjistroHuazimetTableModel.getRegjistroHuazimet(selectedIndex);
                    
                    jDatahuazimit.setDate(regjistroHuazimet.getDataHuazimit());
                    jDateKthimit.setDate(regjistroHuazimet.getDataKthimit());
                   qqCmimi.setText(llogarit(regjistroHuazimet.getDataKthimit(),regjistroHuazimet.getDataHuazimit()));
                  
                    
                   
                }
            }
        });
    }

