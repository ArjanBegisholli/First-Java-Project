    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            int row = tabela.getSelectedRow();
            if (jDatahuazimit.getDate().equals("")) {
                JOptionPane.showMessageDialog(this, "Caktoni Daten e Huazimit ! :", "Error", JOptionPane.ERROR_MESSAGE);

            } else if (jDateKthimit.getDate().equals("")) {
                JOptionPane.showMessageDialog(this, "Caktoni Daten e Kthimit !  :", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (row == -1) {
                    RegjistroHuazimet regjistroHuazimet = new RegjistroHuazimet();
                    
                    regjistroHuazimet.setEmriKlientit(Split((String)txtKlienti.getSelectedItem(),0));
                    regjistroHuazimet.setMbiemriKlientit(Split((String)txtKlienti.getSelectedItem(),1));
                    regjistroHuazimet.setNrPersonal(Split((String)txtKlienti.getSelectedItem(),2));
                    regjistroHuazimet.setLlojivetures(Split((String)txtVetura.getSelectedItem(),0));
                    regjistroHuazimet.setModeli(Split((String)txtVetura.getSelectedItem(),1));
                    regjistroHuazimet.setNgjyra(Split((String)txtVetura.getSelectedItem(),2));
                    regjistroHuazimet.setRentnDit(Split((String)txtVetura.getSelectedItem(),3));
                    regjistroHuazimet.setDataHuazimit(jDatahuazimit.getDate());
                    regjistroHuazimet.setDataKthimit(jDateKthimit.getDate());
                    qqCmimi.setText(llogarit(regjistroHuazimet.getDataKthimit(),regjistroHuazimet.getDataHuazimit()));
                    regjistroHuazimet.setCmimi(Float.parseFloat(qqCmimi.getText()));

                    regjistroHuazimetRepository.create(regjistroHuazimet);
                    JOptionPane.showMessageDialog(this, "E dhëna u ruajt me sukses !");
                } else {
                    RegjistroHuazimet regjistroHuazimet = this.regjistroHuazimetTableModel.getRegjistroHuazimet(row);
                  
                    regjistroHuazimet.setEmriKlientit(Split((String)txtKlienti.getSelectedItem(),0));        
                    regjistroHuazimet.setMbiemriKlientit(Split((String)txtKlienti.getSelectedItem(),1));
                    regjistroHuazimet.setNrPersonal(Split((String)txtKlienti.getSelectedItem(),2));
                    regjistroHuazimet.setLlojivetures(Split((String)txtVetura.getSelectedItem(),0));
                    regjistroHuazimet.setModeli(Split((String)txtVetura.getSelectedItem(),1));
                    regjistroHuazimet.setNgjyra(Split((String)txtVetura.getSelectedItem(),2));
                    regjistroHuazimet.setRentnDit(Split((String)txtVetura.getSelectedItem(),3));
                    regjistroHuazimet.setDataHuazimit(jDatahuazimit.getDate());
                    regjistroHuazimet.setDataKthimit(jDateKthimit.getDate());
                    qqCmimi.setText(llogarit(regjistroHuazimet.getDataKthimit(),regjistroHuazimet.getDataHuazimit()));
                    regjistroHuazimet.setCmimi(Float.parseFloat(qqCmimi.getText()));
                    
                    regjistroHuazimetRepository.edit(regjistroHuazimet);
                    JOptionPane.showMessageDialog(this, "E dhëna u editua me sukses");
                }
                emptyObject();
                tabelaLoad();
            }

        } catch (RegjistroPuntorinException pe) {
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            int row = tabela.getSelectedRow();
            if(row > -1){
                Object [] ob = {"Po","Jo"};
                int i = javax.swing.JOptionPane.showOptionDialog(this, "A dëshironi ta fshini ?", "Fshirja", JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
                if(i==0){
                    RegjistroHuazimet regjistroHuazimet = this.regjistroHuazimetTableModel.getRegjistroHuazimet(row);
                    regjistroHuazimetRepository.remove(regjistroHuazimet);
                    tabelaLoad();
                    emptyObject();
                    JOptionPane.showMessageDialog(this, "E dhëna është fshir me sukses !");
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Nuk keni selektuar asgjë për të fshir !");
            }
        }catch(RegjistroPuntorinException pe){
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

